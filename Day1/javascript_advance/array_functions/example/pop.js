const plants = ["broccoli", "cauliflower", "cabbage", "kale", "tomato"];
console.log(plants.pop()); // > "tomato"
console.log(plants); // > Array ["broccoli","cauliflower","cabbage","kale"]

// pop() dùng để xóa phần tử cuối cùng ra khỏi mảng 
// pop() làm thay đổi mảng ban đầu 
// pop() trả về phần tử đã bị xóa ra khỏi mảng 
// pop() trả về undefined nếu mảng rỗng 